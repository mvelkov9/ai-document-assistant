export const i18n = createI18n({
  legacy: false,
  locale: defaultLocale,
  fallbackLocale: 'sl',
  messages,
})

function getByPath(source, path) {
  return path.split('.').reduce((value, part) => value?.[part], source)
}

export function setI18nLanguage(language) {
  const nextLanguage = language === 'en' ? 'en' : 'sl'
  i18n.global.locale.value = nextLanguage
  document.documentElement.lang = nextLanguage
  return nextLanguage
}

export function translate(language, key, params = {}) {
  setI18nLanguage(language)
  const translated = i18n.global.t(key, params)
  if (translated !== key) return translated
  const template =
    getByPath(messages[language] || messages.sl, key) ?? getByPath(messages.sl, key) ?? key
  return Object.entries(params).reduce(
    (value, [param, replacement]) => value.replaceAll(`{${param}}`, String(replacement)),
    template,
  )
}

export function getSlovenianPluralForm(count, forms) {
  const mod100 = Math.abs(count) % 100
  if (mod100 === 1) return forms[0]
  if (mod100 === 2) return forms[1]
  if (mod100 === 3 || mod100 === 4) return forms[2]
  return forms[3]
}

export function getEnglishPluralForm(count, forms) {
  return Math.abs(count) === 1 ? forms[0] : forms[1]
}

export function formatCountLabel(language, count, forms) {
  const word =
    language === 'en'
      ? getEnglishPluralForm(count, forms.en)
      : getSlovenianPluralForm(count, forms.sl)
  return `${count} ${word}`
}
